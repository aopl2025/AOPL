          <h1 class="display-4 text-center">Republic Act 11361: Anti-Obstruction of Power Lines Act</h1>
          <p class="lead text-center">An Act Ensuring the continuous and uninterrupted transmission and distribution of electricity and the protection of the integrity and reliability of power lines, and providing penalties for violations thereof.</p>
        </div>
        <div class="mt-3" style="background-color:white;padding:50px;">
          <br/>
          <div class="row">
            <div class="col-12 text-center" style="font-size:10pt;">
              <img src="congress.png" alt="">
              <br/>Seventeenth Congress
              <br/>Third Regular Session
              <br/>
              <br/>Begun and held in Metro Manila, on Monday, the twenty-third day of July, two thousand eighteen.
              <br/>
              <br/>[ REPUBLIC ACT NO. 11361, August 08, 2019 ]
              <br/>
              <br/>AN ACT ENSURING THE CONTINUOUS AND UNINTERRUPTED TRANSMISSION AND DISTRIBUTION OF ELECTRICITY AND THE PROTECTION OF THE INTEGRITY AND RELIABILITY OF POWER LINES, AND PROVIDING PENALTIES FOR VIOLATIONS THEREOF
              <br/>
              <br/>Be it enacted by the Senate and House of Representatives of the Philippines in Congress assembled:
            </div>
          </div>
          <div class="row mt-3" style="font-size:10pt;">
            <div class="col-12">
              <b>Section 1</b>. Short Title. - This Act shall be referred to as the "Anti-Obstruction of Power Lines Act."
              <br/>
              <br/><b>Section 2</b>. Declaration of Policy. - It is hereby declared the policy of the State to ensure uninterrupted conveyance of electricity from generating plants to end users, and protect the integrity and reliability of the country's transmission, sub-transmission, and distribution systems by keeping the land beneath, the air spaces surrounding and the are traversed by power lines clear of dangerous obstructions.
              <br/>
              <br/>The State further recognizes that the continuous conveyance of electricity is a matter of national security and is essential to sustaining the country's economic development. Finally, the State acknowledge the crucial role of property owners in ensuring that power lines remain free of any dangerous and hazardous activities and improvements.
              <br/>
              <br/><b>Section 3</b>. Coverage. - This Act shall cover all power lines and other related facilities for the conveyance of electricity throughout the country.	
              <br/>
              <br/><b>Section 4</b>. Definition of Terms. - For purposes of this Act, the following terms shall be defined as stated below:
          
              <p class="m-3" style="text-indent:-18px">(a) Board of Electrical Engineering refers to the Board created under Republic Act No. 7920, otherwise known as the New Electrical Engineering Law under the general supervision and administrative control of the Professional Regulation Commission;</p>
          
              <p class="m-3" style="text-indent:-18px">(b) Civil Code of the Philippines refers to Republic Act No. 386, as amended;</p>
          
              <p class="m-3" style="text-indent:-18px">(c) Conveyance of electricity refers to the delivery of electricity from the generating plants to the premises of the end user, through power lines.</p>
          
              <p class="m-3" style="text-indent:-18px">(d) Dangerous structure or building refers to those which are structurally unsafe; or which constitute a fire hazard not provided with safe agress, or are otherwise dangerous to human health; or which in relation to existing use constitute a hazard to safety, health, or public welfare, by reason of inadequate maintenance, dilapidation, obsolescence, fire hazard, or abandonment; or which otherwise contribute to the pollution of the site or the community to an intolerable degree. It also refers to any building or structure which has any or all of the conditions or defects described in Section 1.01.08(b), Republic Act No. 6541, otherwise known as the "National Building Code of the Philippines": Provided, That such condition or defect exists to the extent that life, health, property, or safety of the public or its occupants are endangered;</p>
          
              <p class="m-3" style="text-indent:-18px">(e) Distribution lines refers to the portion of the distribution system pertaining to the lines or wires;</p>
          
              <p class="m-3" style="text-indent:-18px">(f) Distribution system refers to the system of wires and associated facilities belonging to a franchised distribution utility extending between the delivery points on the transmission or sub-transmission system or generator connection and the point of connection to the premise of the end user;</p>
          
              <p class="m-3" style="text-indent:-18px">(g) Distribution utility (DU) refers to any electric cooperative, private corporation, or government-owned utility which has a franchise to operate a distribution system including those whose franchise covers economic zones.</p>
          
              <p class="m-3" style="text-indent:-18px">(h) Easement fee refers to the just compensation for the legal easement imposed upon the property of the owner, as determined by the courts in accordance with relevant laws, rules and regulations;</p>
          
              <p class="m-3" style="text-indent:-18px">(i) End users refers to any person or entity requiring the supply and delivery of electricity for its own use;</p>
          
              <p class="m-3" style="text-indent:-18px">(j) Hazardous activities refer to burning of waste and other materials, excavation, quarrying, construction, and other activities of the same nature and character, which are performed partially or wholly inside the power line corridor which threaten or endanger the continuous and uninterrupted conveyance of electricity;</p>
          
              <p class="m-3" style="text-indent:-18px">(k) Hazardous improvements refer to structure buildings, and improvements of whatever kind, whether temporary or permanent in character and regardless of the purpose for which they are intended, which are located partially or wholly inside the power line corridor which threaten or endanger the continuous and uninterrupted conveyance of electricity;</p>
          
              <p class="m-3" style="text-indent:-18px">(l) Joint Congressional Energy Commission (JCEC) refers to the congressional oversight body created under Section 62 of Republic Act No. 9136, otherwise known as the Electric Power Industry Reform Act of 2001, as amended by Republic Act No. 11285, otherwise known as the Energy Efficiency and Conservation Act;</p>
          
              <p class="m-3" style="text-indent:-18px">(m) Other distribution entities refer to entities that own, operate or control one or more distribution systems in the main grid and off-grid other than DUs such as, but not limited to, Qualified Third Parties and those under management contracts for the conveyances of electricity;</p>
          
              <p class="m-3" style="text-indent:-18px">(n) Owner or operator of power lines refers to generation companies, the transmission owner and its concessionaire, DUs, other distribution entities, which have ownership or responsibility for the operation and maintenance of power lines;</p>
          
              <p class="m-3" style="text-indent:-18px">(o) Philippine Electrical Code refers to the set of rules which provide for the safety in electrical installation, construction, operation and maintenance, and the utilization of electricity mandated under the New Electrical Engineering Law;</p>
          
              <p class="m-3" style="text-indent:-18px">(p) Power lines refer to transmission lines, sub-transmission lines, distribution lines, and generation dedicated point to point lines, and other connection assets including the poles and towers used to support the lines, and other related facilities constructed or erected used for the purpose of conveyance of electricity;</p>
          
              <p class="m-3" style="text-indent:-18px">(q) Power line obstruction refers to any hazardous activity or hazardous improvement and other similar circumstances that threaten or endanger the continuous and uninterrupted conveyance of electricity;</p>
          
              <p class="m-3" style="text-indent:-18px">(r) Sub-transmission lines refer to he portion of the sub-transmission system pertaining to the lines or wires;</p>
          
              <p class="m-3" style="text-indent:-18px">(s) Sub-transmission system refers to the portion of the distribution system that is delineated by the connection point to the transmission system and the primary side of all substation transformers;</p>
          
              <p class="m-3" style="text-indent:-18px">(t) Tall growing plants refer to plants, vines, and other plant species, which grow or have the potential to grow more than three (3) meters in height from the ground;</p>
          
              <p class="m-3" style="text-indent:-18px">(u) transmission lines refer to the portion of the transmission system referring to the lines or wires; and</p>
          
              <p class="m-3" style="text-indent:-18px">(v) Transmission system refers to the high voltage backbone system of interconnected transmission lines, substations and related facilities, located in each of Luzon, Visayas, and Mindanao, or as may be otherwise determined by the Energy Regulatory Commission in accordance with Section 45 of Republic Act No. 9136, otherwise known as the Electric Power Industry Reform Act of 2001.</p>
          
              <b>Section 5</b>. Power Line Corridor. - The land beneath, the air spaces surrounding, and the areas traversed by power lines including its horizontal, vertical, and similar clearance requirements shall constitute the power line corridor, which shall at all times be kept clear and free from any power line obstructions, dangerous structures, hazardous activities and improvements, and other similar circumstances in accordance with the provisions of this Act.
              <br/>
              <br/>The horizontal, vertical and other similar clearance requirements that constitute the power line corridor shall be determined by the Board of Electrical Engineering and approved by the Department of Energy (DOE), and shall be in accordance with the current Philippine Electrical Code.
              <br/>
              <br/>In case where the power line corridor is wholly or partially located within a private property not owned by the owner or operator of the power lines, it shall constitute a legal easement upon the private property in accordance with the relevant provisions under Book II, Title VII, Chapter 2 of the Civil Code, except if the owner or operator of the power lines shall acquire, lease or enter into other gratuitous or onerous arrangement with the property owner. In the case of legal easement, the owner of the private property shall be compensated the proper easement fee pursuant to the provisions of the Civil Code and other relevant laws, rules and regulations.		
          
              <br/>
              <br/><b>Section 6</b>. Prohibited Acts. - It is hereby declared unlawful for any person, whether natural or juridical, public or private, to:
          
                <p class="m-3" style="text-indent:-18px">(a) Plant or cause to be planted tall growing plants, including plants of whatever kind, variety, or height within the power line corridor:</p>
          
                <p class="m-3" style="text-indent:-18px">(b) Construct or erect any hazardous improvements within the power line corridor;</p>
          
                <p class="m-3" style="text-indent:-18px">(c) Conduct or perform any hazardous activities within the power line corridor;</p>
          
                <p class="m-3" style="text-indent:-18px">(d) Prevent or refuse duly authorized agents of the owner or operator of power lines, entry to the property in the performance of acts enumerated under Section 7: Provided, That such entry is in accordance with the provisions of Section 8; and</p>
          
                <p class="m-3" style="text-indent:-18px">(e) Perform other analogous acts or activities, which will impair the conveyance of electricity and cause damage to power lines.</p>
          
              <b>Section 7</b>. Prevention and Removal of Power Line Obstruction on Public Property or on Property Owned by Owner or Operator of Power Lines. - In cases where the power line corridor is wholly or partially located within public property or on property owned by the owner or operator of the power lines, the owner or operator of power lines shall have the right to enter the said property to prevent and remove any power line obstruction, and in particular, perform the following acts:
          
              <p class="m-3" style="text-indent:-18px">(a) To conduct maintenance and inspection activities within the power line corridor;</p>
          
              <p class="m-3" style="text-indent:-18px">(b) To conduct repair or restoration activities within the power line corridor;</p>
          
              <p class="m-3" style="text-indent:-18px">(c) To conduct trimming, pruning, cutting, or clearing activities for tall growing plants within the power line corridor without securing prior clearance or permit from, but with due notice to, the Department of Environment and Natural Resources (DENR), Philippine Coconut Authority (PCA) in the case of coconut trees, the concerned local government units (LGUs), and other relevant government agencies.</p>
          
              <p class="m-3" style="text-indent:-18px">(d) To remove, dismantle, or demolish hazardous improvements in accordance with the National Building Code of the Philippines: Provided, That such acts may be carried out without prior clearances or permit from, but with due notice to, the Department of Public Works and Highways (DPWH), Housing and Land Use Regulatory Board (HLURB), and the concerned LGUs;</p>
          
              <p class="m-3" style="text-indent:-18px">(e) To stop, prevent, or prohibit the conduct of hazardous activities; and</p>
          
              <p class="m-3" style="text-indent:-18px">(f) Perform other analogous acts or activities, which will cause the prevention and removal of any power line obstruction.</p>
          
              <b>Section 8</b>. Prevention and Removal of Power Line Obstruction on Private Property. - The owner or operator of power lines shall have the primary duty and responsibility to remove power line obstructions. In cases where the power line corridor is wholly or partially located within a private property, the property owner shall coordinate and assist the owner or operator of the power lines by facilitating the necessary access in order to perform the acts enumerated in Section 7.
              <br/>
              <br/>The property owner shall have the primary duty and responsibility to prevent power line obstructions and to inform the owner or operator of the power line of their existence. In the performance of these acts, the property owner shall be entitled to necessary assistance such as, but not limited to, education, and information, and technical and manpower support from the owner or operator of power lines.ℒαwρhi৷
              <br/>
              <br/>In the event that the property owner fails to perform any duty and responsibility under the preceding paragraphs, the owner or operator of the power lines shall have the right to access the power line corridor in order to undertake the acts enumerated under Section 7: Provided, That the entry to private property may only be effected after due notice to, and proper coordination with the property owner: Provided, however, That the foregoing shall no longer be required to avert an imminent danger posed by a power line obstruction in accordance with Article 432 of the Civil Code.
          
              <br/>
              <br/><b>Section 9</b>. Assistance from Local Government Units, Philippine National Police, and Armed Forces of the Philippines. - In the performance of this acts under Sections 7 and 8, the owner or operator of the power lines may seek the assistance of LGU officials, the Philippine National Police (PNP), or the Armed Forces of the Philippines (AFP): Provided, That the LGU officials, the PNP, or the AFP shall render such assistance, as may be deemed necessary.
          
              <br/>
              <br/><b>Section 10</b>. Information, Education, and Communication. - The owner or operator of the power lines shall have the duty and responsibility to inform, educate, and communicate to individuals and communities in the areas ir serves the danger of existing power line obstructions, the urgency of removing them, the manner of preventing them, and other important related information.
          
              <br/>
              <br/><b>Section 11</b>. Resettlement. - Persons residing in hazardous improvements shall be relocated in accordance with Republic Act No. 7279, otherwise known as the "Urban Development and Housing Act of 1992," as amended.
          
              <br/>
              <br/><b>Section 12</b>. Gathering, Collecting, Removing,and Transporting of Timber, Forest Products, and Coconut Lumber. - Gathering, collecting, removing, and transporting of timber, forest products, and coconut lumber as a result of acts performed under Sections 7 and 8 shall not be considered illegal acts: Provided, That due notice is given to the DENR, and the PCA in the case of coconut lumber, and other relevant government agencies.
          
              <br/>
              <br/><b>Section 13</b>. Right of Eminent Domain. - Subject to the limitations and procedures prescribed by the law, the power to exercise the right to eminent domain granted to any person with a franchise to operate, manage, or maintain the electric power lines shall be governed by the pertinent provisions of the Rules of Court.
          
              <br/>
              <br/><b>Section 14</b>. Permits. - Building officials of LGUs shall require the building owner who will be undertaking construction or maintenance activities surrounding, adjacent, or proximate to the power line corridor to give due notice to, and coordinate with the concerned owner or operator of the power lines prior to the issuance of a building permit.
          
              <br/>
              <br/><b>Section 15</b>. Penalties. - Without prejudice to civil and administrative liability, the following penalties shall be imposed upon any person, both natural and juridical, found guilty of committing any of the prohibited acts specified in Section 6 of this Act.
          
              <p class="m-3" style="text-indent:-18px">(a) First Offense - the penalty of arresto mayor or a fine of Fifty thousand pesos (P50,000.00), or both, at the discretion of the court;</p>
          
              <p class="m-3" style="text-indent:-18px">(b) Second Offense - the penalty of prison correccional or a fine of One hundred thousand pesos (100,000,00), or both, at the discretion of the court; and</p>
          
              <p class="m-3" style="text-indent:-18px">(c) Third Offense - the penalty of prison mayor or a fine of Two hundred thousand pesos (P200,000.00), or both, at the discretion of the court.</p>
          
              If the offenses are committed by or in conspiracy with an officer or employee of the owner or operator of the power line, such officer or employees shall suffer a penalty one (1) degree higher than the penalty provided herein.
              <br/>
              <br/>The local building official who issued a building permit n violation of Section 14 of this Act shall be meted a penalty one (1) degree higher than the penalty provided herein and shall forthwith suffer the penalty of dismissal.
          
              <br/>
              <br/><b>Section 16</b>. Congressional Oversight. - The Joint Congressional Energy Commission shall exercise oversight powers over implementation of this Act.
          
              <br/>
              <br/><b>Section 17</b>. Implementing Rules and Regulations. - The DOE shall, in consultation with the National Electrification Administration, National Power Corporation, Board of Electrical Engineering. National Transmission Corporation, DPWH, National Housing Authority, HLURB, Housing and Urban Development Coordinating Council, DENR, PCA, National Commission on the Indigenous Peoples, PNP, AFP, electric power industry stakeholders, and private stakeholders, issue the implementing rules and regulations of this Act within one hundred twenty (120) days upon the effectivity of this Act.
          
              <br/>
              <br/><b>Section 18</b>. Separability Clause. - Any portion or provision of this Act, which may be declared unconstitutional or invalid shall not have the effect of nullifying other portions or provisions hereof.
          
              <br/>
              <br/><b>Section 19</b>. Repealing Clause. - The relevant provisions of Presidential Decree No. 705, as amended, otherwise known as the Revised Forestry Code of the Philippines, Republic Act No. 8048, as amended, otherwise known as the "Coconut Preservation Act of 1995," and the "Urban Development and Housing Act of 1992" are hereby amended. All other laws, ordinances, rules, regulations, and other issuance or parts thereof, which are inconsistent with this Act, are hereby amended, modified, or repealed accordingly.
          
              <br/>
              <br/><b>Section 20</b>. Effectivity. - This Act shall take effect fifteen (15) days after its publication in at least two (2) newspapers of general circulation.
